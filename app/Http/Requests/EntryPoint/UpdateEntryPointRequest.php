<?php

namespace App\Http\Requests\EntryPoint;

use Illuminate\Foundation\Http\FormRequest;

class UpdateEntryPointRequest extends FormRequest
{
    /**
     * Indicates if the validator should stop on the first rule failure.
     *
     * @var bool
     */
    protected $stopOnFirstFailure = true;

    /**
     * Determine if the user is authorized to make this request.
     *
     * @return bool
     */
    public function authorize()
    {
        return true;
    }

    /**
     * Get the validation rules that apply to the request.
     *
     * @return array
     */
    public function rules()
    {
        return [
            'lat'=>['bail','required','numeric', 'max:999999999'],
            'long'=>['bail','required','numeric', 'max:999999999'],
            'bus_route_id'=>['bail','required','exists:bus_routes,id'],

        ];
    }

    /**
     * Get custom attributes for validator errors.
     *
     * @return array
     */
    public function attributes()
    {
        return [
            'lat'=>'',
            'long'=>'',
            'bus_route_id'=>'',

        ];
    }

    /**
     * Get the error messages for the defined validation rules.
     *
     * @return array
     */
    public function messages()
    {
        return [];
    }
}
